
import { getDaysOfWeek, getHoursBetween } from '../src/date-utils'

require('jest-mock-now')(new Date('2023-04-20'));


describe("Date utils", () => {

    test("Get days of week", () => {

        expect(getDaysOfWeek()).toStrictEqual([
            '2023-04-17',
            '2023-04-18',
            '2023-04-19',
            '2023-04-20',
            '2023-04-21',
            '2023-04-22',
            '2023-04-23',
        ]);

        expect(getDaysOfWeek('2023-05-24')).toStrictEqual([
            '2023-05-22',
            '2023-05-23',
            '2023-05-24',
            '2023-05-25',
            '2023-05-26',
            '2023-05-27',
            '2023-05-28',
        ]);

        expect(getDaysOfWeek('2023-05-28')).toStrictEqual([
            '2023-05-22',
            '2023-05-23',
            '2023-05-24',
            '2023-05-25',
            '2023-05-26',
            '2023-05-27',
            '2023-05-28',
        ]);
        
    });

    test("getHoursBetween", () => {

        expect(getHoursBetween(0, 24)).toStrictEqual([
            '00:00', '01:00', '02:00', '03:00', '04:00', '05:00', '06:00', 
            '07:00', '08:00', '09:00', '10:00', '11:00', '12:00', '13:00', 
            '14:00', '15:00', '16:00', '17:00', '18:00', '19:00', '20:00',
            '21:00', '22:00', '23:00', '24:00'
        ]);

        expect(getHoursBetween(8, 20)).toStrictEqual([
            '08:00', '09:00', '10:00', '11:00', '12:00', '13:00', '14:00', 
            '15:00', '16:00', '17:00', '18:00', '19:00', '20:00'
        ]);

    });

});


